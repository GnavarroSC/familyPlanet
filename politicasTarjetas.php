                    </li>
                    <li>
                        El saldo de la Tarjeta Recargable Family Planet puede ser utilizado mientras se encuentre abierto al público el C.E.F., sin restricción horaria, siempre y cuando la carga acumulada cubra el valor de la jugada y la tarjeta se encuentre activa.
                    </li>
                    <li>
                        El saldo de la Tarjeta Recargable Family Planet puede ser consultado en las taquillas, ATM (Cajero Automático de Consulta y Recarga) y en los lectores de consulta de saldo de los C.E.F.
                    </li>
                    <li>
                        Los tickets de puntos obtenidos de los equipos de premios pueden ser cambiados inmediatamente o ser acumulados como Tickets de Puntos en la Tarjeta Recargable Family Planet, para ser consumidos posteriormente hasta máximo un  (1) año desde que se hubieren cargado. Transcurrido dicho término serán eliminados del saldo de la Tarjeta.
                    </li>
                    <li>
                        La Tarjeta Recargable Family Planet podrá ser cambiada, sin costo alguno, cuando la banda magnética o el código de barras no pueda ser leído por los lectores y el saldo será trasladado a la nueva tarjeta.  Para realizar este cambio se debe hacer entrega de la tarjeta averiada.
                    </li>
                    <li>
                        En caso que el daño en la banda magnética o código de barras sea  atribuible al portador, éste correrá con el costo del nuevo plástico. Pagado este valor, se trasladará el saldo a la nueva Tarjeta.
                    </li>
                    <li>
                        En caso que la Tarjeta presente cualquier otro problema de carácter técnico o tecnológico que impida su utilización, Family Planet podrá cambiarla sin costo alguno, y trasladará su saldo a esta nueva tarjeta.
                    </li>
                    <li>
                        El saldo de varias tarjetas puede consolidarse en una sola Tarjeta Recargable Family Planet por solicitud del usuario presentando las tarjetas en la taquilla de los C.E.F. Al usuario le será entregada la tarjeta con el saldo consolidado y las demás las conservará Family Planet.
                    </li>
                    <li>
                        De no usarse la Tarjeta por un período de un (1) año quedará inactiva y, por tanto, será eliminada del sistema, anulándose cualquier saldo acumulado que pudiera haber en dicha Tarjeta. Las tarjetas inactivas por un periodo de un año, quedaran eliminadas del sistema y su saldo será anulado perdiéndose los saldos a favor acumulados. Family Planet no restituirá al usuario ninguna suma de dinero por la anulación de la Tarjeta transcurrido este período de tiempo salvo que la imposibilidad de uso de la Tarjeta haya sido generada por Family Planet.
                    </li>
                    <li>
                        Como la Tarjeta Recargable puede usarse en cualquier momento, los saldos que se encuentran en la tarjeta no serán cambiados por efectivo o por productos diferentes al especificado para el respectivo saldo.
                    </li>
                    <li>
                        Las promociones que otorgue Family Planet no son acumulables, ni se combinan los servicios o productos ofrecidos en una promoción con los de otra.
                    </li>
                    <li>
