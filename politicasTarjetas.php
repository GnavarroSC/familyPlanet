                </p>
                <ol>
                    <li>
                        Para utilizar atracciones y juegos que se encuentran en el Centro de Entretenimiento Familiar “C.E.F.” es indispensable adquirir la Tarjeta Recargable Family Planet y cargarla exclusivamente en las taquillas del C.E.F. con un monto mínimo de carga de $10.000 (Diez Mil pesos).
                    </li>
                    <li>
                        La Tarjeta cargada con créditos se irá consumiendo en la medida que se vayan utilizando las atracciones, juegos del C.E.F. y plaza de comidas (Family Kitchen).  Cada uno de estos juegos y atracciones  consumen créditos de acuerdo al precio para su uso los cuales serán descargados de la tarjeta.  La tarjeta con créditos no aplica para alimentos y fotografía.
                    </li>
                    <li>
